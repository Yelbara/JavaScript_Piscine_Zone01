function more(n)
{
    return n + 1;
}

function less(n)
{
    return n - 1;
}
function add(n1, n2)
{
    return n1 + n2;
}

function sub(n1, n2)
{
    return n1 - n2;
}
console.log("more(5): " + more(5));
console.log("less(5): " + less(5));
console.log("add(5, 3): " + add(5, 3));
console.log("sub(5, 3): " + sub(5, 3));
